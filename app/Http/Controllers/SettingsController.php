    public function changePassword(Request $request) {
        $user = Auth::user();

        if (!Hash::check($request->old_password, $user->password)) {
            return back()->withErrors(['old_password'=>'Password not math']);
        }

        $user->password = Hash::make($request->new_password);

        $user->save();

        return redirect()->back()->with(['changed_password' => 'Success! We changed your password!']);
    }
